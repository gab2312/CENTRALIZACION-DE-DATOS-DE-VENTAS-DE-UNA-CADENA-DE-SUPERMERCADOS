    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        NegProducto negProducto = new NegProducto();

        try {
            negProducto.setCodMarca(DatMarca.getMarcaID(comboMarca, conexion));
            negProducto.setCodSucursal(DatSucursal.getSucursalID(comboSucursal, conexion));
            negProducto.setTipoImpuesto(DatImpuesto.getImpuestoID(comboImpuesto, conexion));
            negProducto.setStockInicial(Long.parseLong(txtStockInicial.getText()));
            negProducto.setStockMinimo(Long.parseLong(txtStockMinimo.getText()));
            String fechaNull = "";
            String fecha = "";
            if (jDateChooser1.getDate() == null) {
                fechaNull = "18000101";
            } else {
                int anio = jDateChooser1.getCalendar().get(Calendar.YEAR);
                int mes = jDateChooser1.getCalendar().get(Calendar.MONTH);
                int dia = jDateChooser1.getCalendar().get(Calendar.DAY_OF_WEEK);
                fecha = anio + "" + mes + "" + dia;
            }
            negProducto.setFechaVencimiento(isNull(fecha, fechaNull));
            negProducto.setNombreProducto(txtNombreProducto.getText());
            negProducto.setPrecioUnitario(Long.parseLong(txtPrecioProducto.getText()));

            if (txtStockInicial.getText().isEmpty() || txtNombreProducto.getText().isEmpty()
                    || txtStockMinimo.getText().isEmpty() || txtPrecioProducto.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Existe algun campo obligatorio vacÃ­o", "Error!", JOptionPane.ERROR_MESSAGE);
            } else if (Color.RED == jDateChooser1.getDateEditor().getUiComponent().getForeground()) {
                JOptionPane.showMessageDialog(this, "Formato de fecha invalido");
            } else if (negProducto.insertProducto(negProducto)) {
                JOptionPane.showMessageDialog(this, "Producto ingresado Correctamente");
            }

        } catch (SQLException | NumberFormatException | HeadlessException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }


    }//GEN-LAST:event_jButton1ActionPerformed


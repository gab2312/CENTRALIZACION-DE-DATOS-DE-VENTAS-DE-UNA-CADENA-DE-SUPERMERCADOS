    protected void validarAlphabeto(KeyEvent evt) throws HeadlessException {
        char c = evt.getKeyChar();

        if ((int) evt.getKeyChar() > 32 && (int) evt.getKeyChar() <= 47
                || (int) evt.getKeyChar() >= 58 && (int) evt.getKeyChar() <= 64
                || (int) evt.getKeyChar() >= 91 && (int) evt.getKeyChar() <= 96
                || (int) evt.getKeyChar() >= 123 && (int) evt.getKeyChar() <= 255) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(this, "Solo son permitidos datos alphanumericos, no incluir tildes ni apstrofes");
            txtNombreProducto.setCursor(null);
        }
    }

    protected String isNull(String a, String b) {
        if (a.isEmpty()) {
            return b;
        } else {
            return a;
        }
    }

    protected void validarNumerico(KeyEvent evt) {
        if (!Character.isDigit(evt.getKeyChar()) && !Character.isISOControl(evt.getKeyChar())) {
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
        }
    }


